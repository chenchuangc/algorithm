public class ThreeNode {
    char key;
    Object val;
    ThreeNode left;
    ThreeNode mid;
    ThreeNode right;

    public char getKey() {
        return key;
    }

    public void setKey(char key) {
        this.key = key;
    }

    public Object getVal() {
        return val;
    }

    public void setVal(Object val) {
        this.val = val;
    }

    public ThreeNode getLeft() {
        return left;
    }

    public void setLeft(ThreeNode left) {
        this.left = left;
    }

    public ThreeNode getMid() {
        return mid;
    }

    public void setMid(ThreeNode mid) {
        this.mid = mid;
    }

    public ThreeNode getRight() {
        return right;
    }

    public void setRight(ThreeNode right) {
        this.right = right;
    }
}
